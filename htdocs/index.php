<h2>Project Objectives</h2>
<ol>
<li>Develop standardized methodologies for estimating C, N, and water footprints of corn 
production in the region and perform baseline monitoring.</li>

<li>Perform field tests across 21 baseline sites in eight states to evaluate the impacts 
of a suite of crop management practices (including no-till, extended crop rotations, 
drainage water management, cover crops, and canopy N-sensors) on C, N, and water footprints.</li>
 
<li>Apply climate and physical models to synthesize results from the field tests and extend 
them to predict climate and economic scenarios.</li>

<li>Perform comprehensive life cycle analyses (LCA) of the proposed practices and evaluate the 
socio-economic-environmental willingness of producers and farmers to adopt new cropping systems 
through feedback loops between social science research, biophysical field research, monitoring, 
and modeling of agricultural production systems.</li>

<li>Integrate education, extension, outreach, and stakeholder participation across all 
aspects of the program. Focus will be on place-based education and outreach programs. 
Farmers will participate via <a href="http://i-farmtools.org/">I-FARM</a>, an interactive tool to 
analyze the economic, agronomic, and social acceptability of these practices.</li>

</ol>


